#include <iostream>
#include <string.h>
using namespace std;

int N, dp[101][10][2][2], ans = 0;

int main()
{
	memset(dp, 0, sizeof(dp));
	cin >> N;
	if (N < 10)
	{
		cout << 0;
		return (0);
	}
	for (int i = 1; i < 9; i++)
		dp[1][i][0][0] = 1;
	dp[1][9][0][1] = 1;

	for (int i = 2; i < N + 1; i++)
	{
		for (int j = 0; j < 10; j++)
		{
			if (j == 0)
			{
				dp[i][0][1][1] += dp[i - 1][1][0][1] + dp[i - 1][1][1][1];
				dp[i][0][1][0] += dp[i - 1][1][0][0] + dp[i - 1][1][1][0];
			}
			else if (j == 9)
			{
				dp[i][9][1][1] += dp[i - 1][8][1][0] + dp[i - 1][8][1][1];
				dp[i][9][0][1] += dp[i - 1][8][0][0] + dp[i - 1][8][0][1];
			}
			else
			{
				dp[i][j][0][0] += dp[i - 1][j - 1][0][0] + dp[i - 1][j + 1][0][0];
				dp[i][j][0][1] += dp[i - 1][j - 1][0][1] + dp[i - 1][j + 1][0][1];
				dp[i][j][1][0] += dp[i - 1][j - 1][1][0] + dp[i - 1][j + 1][1][0];
				dp[i][j][1][1] += dp[i - 1][j - 1][1][1] + dp[i - 1][j + 1][1][1];
			}
			dp[i][j][0][0] %= 1000000000;
			dp[i][j][1][0] %= 1000000000;
			dp[i][j][0][1] %= 1000000000;
			dp[i][j][1][1] %= 1000000000;
		}
	}

	for (int i = 0; i < 10; i++)
	{
		// cout << i << " : " << dp[N][i][1][1] << endl;
		ans += dp[N][i][1][1];
		// ans += dp[N][i][1023];
		ans %= 1000000000;
	}

	cout << ans;

	return (0);
}

