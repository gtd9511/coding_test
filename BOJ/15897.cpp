#include <iostream>
#include <cmath>
using namespace std;

int main()
{
	int n, ans = 0;
	cin >> n;
	int prev = n;
	// if (n == 3)
	// {
	// 	cout << 6;
	// 	return (0);
	// }
	// if (n == 4)
	// {
	// 	cout << 9;
	// 	return (0);
	// }
	for (int i = 1; i <= static_cast<int>(sqrt(n)); i++)
	{
		int temp = 0;
		temp = n / i;
		// cout << prev - n / i << endl;
		if (n % i != 0)
			temp++;
		ans += temp;
		if (i != 1)
		{
			ans += (prev - temp) * i;
			prev = temp;
		}
		else
			ans++;

		// cout << "i : " << i << " n/i : " << n / i << " ans : " << ans << endl;

	}
	cout << ans;
	return (0);
}


